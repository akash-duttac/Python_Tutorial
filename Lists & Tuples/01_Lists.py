# list is created using [] square brackets
# remember whitespaces after ,
list1 = [2, 3, 4, 5, 21]
print("The list is: ")
print(list1)

# access list members/elements using index
print("List[4] = " + str(list1[4]))
print("List[2] = " + str(list1[2]))

# index can be used to change the values
list1[0] = 2000
print("Updated list: " + str(list1))

# list can consist of different data types
list2 = [27, "June", True, 3.14]
print("List2 = " + str(list2))