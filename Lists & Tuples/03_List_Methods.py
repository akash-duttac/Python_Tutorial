li = [23, 54, 1, 243, 99, 104]
print(li)

li.sort() #sorts the list
print(li)

li.reverse() # reverses the list
print(li)

li.append(2000) # will add 2000 in the end of list
print(li)

li.insert(3, 100) # insert 100 at index 3
print(li)

li.pop(3) # pops the element at index 3
print(li)

li.remove(243) #removes the element 243
print(li)
